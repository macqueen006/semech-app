                            <h3>Verification: The Foundation of Trust</h3>

                            <p>
                                Our methodology works because every piece of content undergoes rigorous verification. Teams verify events and place them into an evidentiary database. All contributions are vetted by trained researchers. This is why international courts would accept the data as evidence.
                            </p>

                            <p>
                                Which is why the verification process is up to standard:
                            </p>

                            <h4>Multi-Source Verification Process</h4>

                            <ul role="list">
                                <li><strong>Geolocation</strong>: Every infrastructure point or incident must be confirmed using satellite imagery (Google Earth, Sentinel satellites), street view data, and landmark cross-referencing</li>
                                <li><strong>Visual evidence</strong>: Photos and videos with metadata intact (location stamps, date stamps, device information)</li>
                                <li><strong>Official data cross-check</strong>: Comparing citizen reports against government databases, budget documents, and official announcements</li>
                                <li><strong>Community validation</strong>: Local teams in each state reviewing submissions from their areas before publication</li>
                                <li><strong>Timestamp verification</strong>: Confirming when events occurred using shadows in photos, weather data, and other chronolocation techniques</li>
                            </ul>

                            <p>
                                Nothing appears on the public map until it passes all verification checks. Every pin shows its sources, verification date, and confidence level.
                            </p>

