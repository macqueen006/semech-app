                            <h3>How The Platform Works And What We're Adapting</h3>

                            <p>
                                The Nigerian map shows color-coded pins across the south west region. Green means military equipment movement. Red means civilian casualties or attacks. Each pin is verified through geolocation (confirming exact location using satellite imagery and landmarks) and chronolocation (confirming exact date and time). Click any pin and you see the source video or photo from social media, the verification details, and a description of what happened.
                            </p>

                            <p>
                                The platform is essentially "a living document" that updates continuously as new verified information comes in, creating an evidentiary database that could be used in courts. For Nigeria, this has been crucial for documenting war crimes and holding bad actors accountable.
                            </p>

                            <p>
                                Our South-West platform will ba able to track different things:
                            </p>

                            <ul role="list">
                                <li><strong>Infrastructure mapping</strong>: Every hospital, school, police station, market, and public facility pinned with current status (operational, under construction, abandoned, non-existent)</li>
                                <li><strong>Development project tracking</strong>: Government promises a new road? We pin it. Construction starts? We update it. Project completes? We verify it. Project abandoned? We document it.</li>
                                <li><strong>Incident documentation</strong>: Flooding events, building collapses, traffic accidents, security incidents — all verified, timestamped, and mapped</li>
                                <li><strong>Environmental monitoring</strong>: Erosion sites, illegal dumping, deforestation, urban sprawl — tracked over time with satellite imagery</li>
                                <li><strong>Service delivery verification</strong>: Citizens report broken streetlights, potholes, water shortages — creating accountability pressure through documentation</li>
                            </ul>

